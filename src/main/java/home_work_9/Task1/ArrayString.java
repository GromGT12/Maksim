package home_work_9.Task1;

public class ArrayString {
    public static void main(String[] args) {
        String s1 = "papa";
        int len1 = s1.length();
        String s2 = "kot";
        int len2 = s2.length();
        String s3 = "program";
        int len3 = s3.length();
        String s4 = "na";
        int len4 = s4.length();
        System.out.println("papa: " + len1);
        System.out.println("kot: " + len2);
        System.out.println("program: " + len3);
        System.out.println("na: " + len4);
    }
}
