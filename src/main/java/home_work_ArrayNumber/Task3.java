package home_work_ArrayNumber;

class Task3 {
    public static void main(String[] args) {
        int[] array = new int[]{66, 77, 90, 3, 15, 33, 45, 0, -20};
        for (int i = 0; i <= 0; i++) {
            int myInt = array[0];
            System.out.printf("Элемент %d массива %d", i, myInt);
            System.out.println();
            int result = myInt / 3;
            System.out.printf("Результат деления %d на 3: %d", myInt, result);
            System.out.println();
        }
    }
}


